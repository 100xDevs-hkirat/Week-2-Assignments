const todosRouter = require('./routes/todosRouter.js')
const express = require('express');
const bodyParser = require('body-parser');

const app = express();
// ROUTES

app.use(bodyParser.json());

app.use('/todos', todosRouter)
app.all('*', (req, res) => {
  res.status(404).json({msg: 'PAGE NOT FOUND'})
});

module.exports = app;
