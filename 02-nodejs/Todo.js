/*
  Each todo has a title and a description. The title is a string and the description is a string.
  Each todo should also get an unique autogenerated id every time it is created
  Implement a class `Todo` having below methods
    - add(todo): adds todo to list of todos
    - remove(indexOfTodo): remove todo from list of todos
    - update(index, updatedTodo): update todo at given index
    - getAll: returns all todos
    - get(indexOfTodo): returns todo at given index
    - clear: deletes all todos

  Once you've implemented the logic, test your code by running
  - `npm run test-todo-list`
*/

const fs = require('fs');

class Todo {
    constructor() {
        this.todos = [];
        this.filename = './todos.json';
        this.loadTodos();
    }

    loadTodos() {
        if (fs.existsSync(this.filename)) {
            const data = fs.readFileSync(this.filename, 'utf8');
            if(data){
                this.todos = JSON.parse(data);
            }
        }
    }

    saveTodos() {
        fs.writeFileSync(this.filename, JSON.stringify(this.todos), 'utf8');
    }

    add(todo) {
        const newTodo = { ...todo, id: this.generateId() };
        this.todos.push(newTodo);
        this.saveTodos();
    }

    remove(idInput) {
        const index = this.todos.findIndex(function (item) {
            return item.id === idInput;
        });
        if (index >= 0 && index < this.todos.length) {
            this.todos.splice(index, 1);
            this.saveTodos();
        } else {
            return -1;
        }
    }

    update(idInput, updatedTodo) {
        const index = this.todos.findIndex(function (item) {
            return item.id === idInput;
        });
        if (index >= 0 && index < this.todos.length) {
            this.todos[index] = { ...updatedTodo, id: this.todos[index].id };
            this.saveTodos();
            return index;
        } else {
            return -1;
        }
    }

    getAll() {
        return this.todos;
    }

    get(idInput) {
        const indexOfTodo = this.todos.findIndex(function (item) {
            return item.id === idInput;
        });
        if (indexOfTodo >= 0 && indexOfTodo < this.todos.length) {
            return this.todos[indexOfTodo];
        }
        return null;
    }

    clear() {
        this.todos = [];
        this.saveTodos();
    }

    generateId() {
        const timestamp = new Date().getTime();
        const randomId = Math.floor(Math.random() * 1000);
        return `${timestamp}-${randomId}`;
    }
}


